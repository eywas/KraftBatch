package day02_MyfirstProgram.src.day03_EscapeChar_Variables;

public class Er_Vs_Out {

    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println("---------------------------");
        System.err.println("Hello World!");
    }
}
/*
              System.out.println() : used for regular outputs (white-beyaz)
              System.err.println() : used for printing error messages (red-kırmızı)
              */