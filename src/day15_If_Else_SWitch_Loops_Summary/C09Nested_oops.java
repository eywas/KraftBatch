package day02_MyfirstProgram.src.day15_If_Else_SWitch_Loops_Summary;

public class C09Nested_oops {
    public static void main(String[] args) {
        for (int i = 0; i <3 ; i++) {
            System.out.print("A");
            for (int j = 0; j <2 ; j++) {
                System.out.print("B");
            }
            System.out.println();
        }
    }
}
